<h3>Luisa</h3>

<p>I am a CPCAB Integrative Counsellor and Child Therapist.</p> 
<p>My core Therapeutic Model is Humanistic incorporating elements of Person-Centred Therapy, Transactional Analysis and CBT and Gestalt Therapy. </p>
<p>I work alongside the Client using creative interventions and tools, approach and Modality to the client and their individual needs. I also specialise in working with attachment disorders, trauma, ASD, ADHD, vulnerable families and children. Prior to becoming a counsellor I spent 18 years in the caring profession as a special needs trained NNEB working with children and adults in various nurserys, schools, special needs schools, care homes, children centres working with vulnerable families and victims of trauma and domestic violence.</p>

